#Perbandingan
#lebih dari >
#kurang dari <








a = 3
b = 6
#lebih dari

hasil = a > b
print (a, '>', b, '=', hasil)

#kurang dari
hasil = a < b
print (a, '>', b, '=', hasil)

# lebih dari sama dengan
hasil = a >= b
print (a, '>=', b, '=', hasil)

#kurang dari sama dengan
hasil = a <= b
print (a, '<=', b, '=', hasil)

#is

hasil = a is 3
print (a, 'is', 3, '=', hasil)

#is not
hasil = a is not 4
print (a, 'is not', 4, '=', hasil)